    Profesor personaAEliminar = null;
    public void eliminar(){
        
        for (Profesor persona : Profe) {
            if (persona.getCodigo().equals(txtCodigo.getText())) {
                personaAEliminar = persona;
                break;
            }
        }
        
        if (personaAEliminar != null) {
            Profe.remove(personaAEliminar); // Eliminar el objeto de la lista
           
            JOptionPane.showMessageDialog(null, "Objeto eliminado: " + personaAEliminar.getNombre());
        } else {
            
            JOptionPane.showMessageDialog(null, "No se encontr√≥ el objeto a eliminar.");
        }
        
    
    }
    
    
    
