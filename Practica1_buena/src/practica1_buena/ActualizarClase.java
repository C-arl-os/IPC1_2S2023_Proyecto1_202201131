    ArrayList<String> datos = new ArrayList<>();
    public void cargarcombo() {

        for (int i = 0; i < Profe.size(); i++) {
            datos.add(Profe.get(i).getNombre() + " " + Profe.get(i).getApellido());
        }
        for (String dato : datos) {
            combomaestros.addItem(dato);
        }

    }
     public void cargar(){
    for (int i = 0; i <curso.size(); i++) {
            String codigo;
            codigo = txtCodigo.getText();
            if(codigo.equals(curso.get(i).getCodigo()) ){
                txtNombre.setText(curso.get(i).getNombre());
                txtCreditos.setText(curso.get(i).getCreditos());               
                combomaestros.setSelectedItem(curso.get(i).getProfesor());
                indice=i;
                txtCodigo.setEnabled(false);
                
                
            }else{
                JOptionPane.showMessageDialog(null, "Usuario no encontrado");
            }
            
        }
    
    }
