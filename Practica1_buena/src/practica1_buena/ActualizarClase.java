    public void actu(){
        String codigo = String.valueOf(indice);
        String nombre = txtNombre.getText();
        String creditos = txtCreditos.getText();
        String alumno = curso.get(indice).getAlumnos();
        String Profesor = (String) combomaestros.getSelectedItem();
        String nota = curso.get(indice).nota;
        
        Cursos profe1= new Cursos(codigo, nombre, creditos, alumno, Profesor, nota);
        curso.set(indice, profe1);
        
        txtCodigo.setText("");
        txtCreditos.setText("");
        txtNombre.setText("");
    }
   
   

